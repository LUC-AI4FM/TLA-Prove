"""
dataset_builder.py — Build training-ready JSONL from validated+annotated records.

Converts DatasetRecord objects into four fine-tuning task formats, each
formatted in the gpt-oss harmony prompt structure.  Output is HuggingFace
`datasets`-compatible JSONL with `messages` lists ready for SFTTrainer.

Task types
----------
spec_generation   (∼50% of training examples)
    User: natural language description
    Assistant: complete TLA+ spec

spec_completion   (∼20%)
    User: partial spec (first N lines, typically up to VARIABLES declaration)
    Assistant: rest of the spec

invariant_gen     (∼20%)
    User: full spec + "generate safety invariants for this spec"
    Assistant: invariant operators in valid TLA+ syntax

bug_fix           (∼10%; generated by mutating and re-checking clean specs)
    User: spec with an injected mutation + TLC error message
    Assistant: corrected spec

Harmony format
--------------
gpt-oss will NOT work correctly without the harmony response format.
Every assistant turn must include a channel declaration.
We use:
  analysis  — chain-of-thought reasoning (not shown to end users)
  final     — the actual TLA+ output

The `openai-harmony` package handles template application when the
tokenizer's `apply_chat_template` is called.  We also store raw
`messages` dicts in the JSONL so the trainer can call it directly.

Split strategy
--------------
90/10 train/eval, stratified by (domain, quality.overall) to ensure the
eval set covers all domains and difficulty levels.
"""

from __future__ import annotations

import json
import random
import re
from pathlib import Path
from typing import Iterator

from src.shared.schemas.dataset_schema import DatasetRecord

_REPO_ROOT = Path(__file__).resolve().parents[2]
_COMBINED_JSONL = _REPO_ROOT / "data" / "validated" / "combined.jsonl"
_TRAIN_OUT      = _REPO_ROOT / "data" / "processed" / "train.jsonl"
_EVAL_OUT       = _REPO_ROOT / "data" / "processed" / "eval.jsonl"

# System prompt goes into the harmony 'developer' role so it's developer-
# authored context, not the auto-injected system message.
_DEVELOPER_PROMPT = """\
You are ChatTLA, an expert at writing verified TLA+ formal specifications.
When asked to write a TLA+ spec, follow these rules exactly:
1. Start the module with ---- MODULE <ModuleName> ----
2. End with ====
3. Include EXTENDS, VARIABLES, Init, Next, and Spec operators
4. After the TLA+ module, append a TLC configuration block:
   SPECIFICATION Spec
   INVARIANT TypeOK   (if TypeOK is defined)
5. Output only valid TLA+ code. No markdown fences, no explanation outside the spec.
Reasoning: medium\
"""


def build_messages_spec_generation(record: DatasetRecord) -> list[dict] | None:
    """User: NL description → Assistant: complete spec."""
    if not record.annotation or not record.annotation.natural_language_description:
        return None
    nl = record.annotation.natural_language_description.strip()
    if not nl:
        return None
    return [
        {"role": "developer", "content": _DEVELOPER_PROMPT},
        {"role": "user",      "content": f"Write a TLA+ specification for the following:\n\n{nl}"},
        {"role": "assistant", "channel": "analysis",  "content": "I'll write a well-formed TLA+ specification with proper Init, Next, and invariants."},
        {"role": "assistant", "channel": "final",     "content": record.tla_content.strip()},
    ]


def build_messages_spec_completion(record: DatasetRecord) -> list[dict] | None:
    """User: first portion of spec → Assistant: completed spec."""
    lines = record.tla_content.splitlines()
    if len(lines) < 20:
        return None
    # Find a natural split point: after VARIABLES or after CONSTANTS
    split_idx = len(lines) // 3  # fallback: first third
    for i, line in enumerate(lines):
        if re.match(r"^\s*VARIABLES\b", line, re.IGNORECASE) and i > 4:
            split_idx = i + 1
            break
    prefix = "\n".join(lines[:split_idx])
    return [
        {"role": "developer", "content": _DEVELOPER_PROMPT},
        {"role": "user",      "content": f"Complete this TLA+ specification:\n\n{prefix}"},
        {"role": "assistant", "channel": "analysis",  "content": "I'll complete the specification with proper Next, Spec, and invariant definitions."},
        {"role": "assistant", "channel": "final",     "content": record.tla_content.strip()},
    ]


def build_messages_invariant_gen(record: DatasetRecord) -> list[dict] | None:
    """User: full spec → Assistant: invariant operators."""
    if not record.quality or not record.quality.has_invariants:
        return None
    # Extract invariant operators from the spec
    invs = _extract_invariants(record.tla_content)
    if not invs:
        return None
    return [
        {"role": "developer", "content": _DEVELOPER_PROMPT},
        {"role": "user",      "content": f"Given this TLA+ spec, write safety invariants:\n\n{record.tla_content.strip()}"},
        {"role": "assistant", "channel": "analysis",  "content": "I'll identify the key safety properties and express them as TLA+ invariant operators."},
        {"role": "assistant", "channel": "final",     "content": "\n\n".join(invs)},
    ]


def build_messages_bug_fix(record: DatasetRecord, error_msg: str, buggy_tla: str) -> list[dict]:
    """User: buggy spec + TLC error → Assistant: fixed spec."""
    return [
        {"role": "developer", "content": _DEVELOPER_PROMPT},
        {
            "role": "user",
            "content": (
                f"This TLA+ spec has an error that TLC found:\n\n"
                f"```\n{error_msg}\n```\n\n"
                f"Buggy spec:\n{buggy_tla.strip()}\n\n"
                f"Fix the spec."
            ),
        },
        {"role": "assistant", "channel": "analysis",  "content": "I'll analyse the TLC error and produce a corrected specification."},
        {"role": "assistant", "channel": "final",     "content": record.tla_content.strip()},
    ]


def _extract_invariants(tla_content: str) -> list[str]:
    """Return blocks of text that define invariant operators."""
    blocks: list[str] = []
    lines = tla_content.splitlines()
    i = 0
    while i < len(lines):
        if re.match(r"^(TypeOK|Safety|Inv\w*|.*Invariant)\s*==", lines[i], re.IGNORECASE):
            block_lines = [lines[i]]
            j = i + 1
            while j < len(lines) and (not lines[j].strip() or lines[j].startswith(" ")):
                block_lines.append(lines[j])
                j += 1
            blocks.append("\n".join(block_lines).strip())
            i = j
        else:
            i += 1
    return blocks


# ---------------------------------------------------------------------------
# Stratified 90/10 split
# ---------------------------------------------------------------------------

def stratified_split(records: list[DatasetRecord], eval_fraction: float = 0.1, seed: int = 42) -> tuple[list, list]:
    """
    Split records into train/eval, stratified by (domain, difficulty).
    Falls back to random split if a stratum has fewer than 2 records.
    """
    rng = random.Random(seed)
    strata: dict[tuple, list[DatasetRecord]] = {}
    for r in records:
        domain = (r.annotation.domain if r.annotation else None) or "unknown"
        diff   = (r.annotation.difficulty if r.annotation else 0) or 0
        key = (domain, min(diff, 3))  # bucket difficulty into 0-1, 2-3, 4-5
        strata.setdefault(key, []).append(r)

    train, eval_ = [], []
    for bucket in strata.values():
        rng.shuffle(bucket)
        n_eval = max(1, int(len(bucket) * eval_fraction))
        eval_.extend(bucket[:n_eval])
        train.extend(bucket[n_eval:])

    rng.shuffle(train)
    rng.shuffle(eval_)
    return train, eval_


# ---------------------------------------------------------------------------
# Full build entry point
# ---------------------------------------------------------------------------

def build(
    combined_path: Path = _COMBINED_JSONL,
    train_path: Path = _TRAIN_OUT,
    eval_path:  Path = _EVAL_OUT,
) -> tuple[int, int]:
    """
    Load combined.jsonl → build all task variants → write train/eval JSONL.

    Returns (n_train, n_eval).
    """
    records: list[DatasetRecord] = []
    for line in combined_path.open(encoding="utf-8"):
        line = line.strip()
        if line:
            records.append(DatasetRecord.from_dict(json.loads(line)))

    print(f"[dataset_builder] Loaded {len(records)} records from {combined_path}")

    train_records, eval_records = stratified_split(records)

    def _write(recs: list[DatasetRecord], path: Path) -> int:
        path.parent.mkdir(parents=True, exist_ok=True)
        count = 0
        with path.open("w", encoding="utf-8") as f:
            for r in recs:
                tasks = [
                    build_messages_spec_generation(r),
                    build_messages_spec_completion(r),
                    build_messages_invariant_gen(r),
                ]
                for msgs in tasks:
                    if msgs is not None:
                        f.write(json.dumps({"messages": msgs}, ensure_ascii=False) + "\n")
                        count += 1
        return count

    n_train = _write(train_records, train_path)
    n_eval  = _write(eval_records,  eval_path)
    print(f"[dataset_builder] train={n_train} examples → {train_path}")
    print(f"[dataset_builder] eval ={n_eval}  examples → {eval_path}")
    return n_train, n_eval


if __name__ == "__main__":
    build()
