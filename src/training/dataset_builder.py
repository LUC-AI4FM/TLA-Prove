def _sany_filter(records: list[DatasetRecord]) -> list[DatasetRecord]:
    """
    Filter records to only those whose tla_content passes SANY validation.

    This ensures the model only trains on syntactically valid TLA+ specs,
    which is critical for SANY pass rate improvement.  Records that already
    have a silver/gold tlc_result are accepted without re-validation.
    """
    from src.validators.sany_validator import validate_string as sany_check

    passed: list[DatasetRecord] = []
    skipped = 0

    for r in records:
        # Fast path: trust existing tier if available
        if r.tlc_result and r.tlc_result.tier in ("gold", "silver"):
            passed.append(r)
            continue

        # Otherwise, run SANY on the spec
        m = re.search(r"----\s*MODULE\s+(\w+)", r.tla_content)
        module_name = m.group(1) if m else "Spec"
        try:
            result = sany_check(r.tla_content, module_name=module_name)
            if result.valid:
                passed.append(r)
            else:
                skipped += 1
                log.info(f"  SANY reject: {r.source} — {result.errors[:2]}")
        except Exception as exc:
            skipped += 1
            log.warning(f"  SANY error on {r.source}: {exc}")

    log.info(f"[sany_filter] {len(passed)} passed, {skipped} rejected")
    return passed


def _extract_final_spec_from_messages(ex: dict) -> str:
    msgs = ex.get("messages", [])
    return next(
        (m["content"] for m in msgs if m.get("role") == "assistant" and m.get("channel") == "final"),
        "",
    )


def _prompt_aggregate_key(ex: dict) -> str:
    """Stable key for RL augmented rows: prefer _prompt_id, else hash of user content."""
    pid = ex.get("_prompt_id")
    if pid is not None:
        return str(pid)
    msgs = ex.get("messages", [])
    u = next((m["content"] for m in msgs if m.get("role") == "user"), "")
    return hashlib.sha256(u.encode("utf-8")).hexdigest()[:32]


_TIER_RANK = {"gold": 3, "bugfix": 3, "silver": 2, "bronze": 1, "description_sft": 2}


def _record_module_name(record: DatasetRecord) -> str | None:
    meta = record.metadata or {}
    mn = meta.get("module_name")
    if isinstance(mn, str) and mn.strip():
        return mn.strip()
    return parse_module_name(record.tla_content)


def dedupe_augmented_best_per_prompt(examples: list[dict]) -> list[dict]:
    """
    Keep one row per prompt key — the highest toolchain tier wins (gold ≈ bugfix > silver > bronze).
    Ties: prefer longer final spec (often more complete).
    """
    best: dict[str, tuple[int, int, dict]] = {}
    for ex in examples:
        key = _prompt_aggregate_key(ex)
        tier = (ex.get("_tier") or "gold").lower()
        rank = _TIER_RANK.get(tier, 0)
        spec = _extract_final_spec_from_messages(ex)
        slen = len(spec)
        cur = best.get(key)
        if cur is None:
            best[key] = (rank, slen, ex)
            continue
        cr, cs, _ = cur
        if rank > cr or (rank == cr and slen > cs):
            best[key] = (rank, slen, ex)
    return [t[2] for t in best.values()]


def build(
    combined_path: Path = _COMBINED_JSONL,
    train_path: Path = _TRAIN_OUT,
    eval_path:  Path = _EVAL_OUT,
    sany_only: bool = False,
    include_augmented: bool = False,
    augmented_path: Path = _AUGMENTED_JSONL,
    gold_only_augmented: bool = True,
    include_description_sft: bool = False,
    description_sft_path: Path = _DESCRIPTION_SFT_JSONL,
    include_gold_benchmark: bool = False,
    gold_benchmark_path: Path = _GOLD_BENCHMARK_JSONL,
    bugfix_oversample: int = 2,
    include_silver_augmented: bool = True,
    augmented_best_per_prompt: bool = True,
    skip_model_check_shims: bool = True,
) -> tuple[int, int]:
    """
    Load combined.jsonl → build all task variants → write train/eval JSONL.

    Parameters
    ----------
    sany_only         : bool   If True, filter to only SANY-validated specs before
                               building training examples.  This is the single
                               highest-impact lever for improving SANY pass rate
                               in the fine-tuned model.
    include_augmented : bool   If True, append augmented.jsonl examples to the
                               training set (generated by src.training.augment).
                               These are NOT included in the eval split.
    include_description_sft : bool  If True, append description_sft.jsonl (from
                               scripts/build_description_sft_jsonl.py). Excludes
                               benchmark holdout modules.
    bugfix_oversample : int  Repeat each `_tier` == `bugfix` augmented example
                               this many times (default 2) to emphasize TLC-error
                               → fix learning. Use 1 to disable.
    include_silver_augmented : bool  If True, include RL **silver** rows (SANY-valid,
                               TLC failed). Builds a larger curriculum; can be disabled
                               if TLC quality drops.
    augmented_best_per_prompt : bool  If True, collapse augmented.jsonl to **one row
                               per prompt** (`_prompt_id` or user-text hash), keeping the
                               **best tier** seen across all RL runs (cumulative quality).
    skip_model_check_shims : bool  If True, drop TLC `MC*` wrapper modules (EXTENDS parent
                               + TLC; tiny files). Training on them pairs rich NL with
                               stub specs and poisons spec-generation.

    Returns (n_train, n_eval).
    """
    records: list[DatasetRecord] = []
    for line in combined_path.open(encoding="utf-8"):
        line = line.strip()
        if line:
            records.append(DatasetRecord.from_dict(json.loads(line)))

    print(f"[dataset_builder] Loaded {len(records)} records from {combined_path}")

    if skip_model_check_shims:
        before = len(records)
        kept: list[DatasetRecord] = []
        for r in records:
            mn = _record_module_name(r)
            if is_model_check_shim(mn, r.tla_content):
                continue
            kept.append(r)
        n_skip = before - len(kept)
        records = kept
        if n_skip:
            print(
                f"[dataset_builder] Skipped {n_skip} MC* TLC model-check shims "
                f"(train on parent modules instead; use --no-skip-mc-shims to keep)"
            )

    if sany_only:
        print(f"[dataset_builder] Filtering to SANY-valid specs only...")
        records = _sany_filter(records)
        print(f"[dataset_builder] {len(records)} records passed SANY validation")

    train_records, eval_records = stratified_split(records)

    def _write(recs: list[DatasetRecord], path: Path) -> int:
        path.parent.mkdir(parents=True, exist_ok=True)
        count = 0
        with path.open("w", encoding="utf-8") as f:
            for r in recs:
                tasks = [
                    build_messages_spec_generation(r),
                    build_messages_spec_completion(r),
                    build_messages_invariant_gen(r),
                ]
                for msgs in tasks:
                    if msgs is not None:
                        f.write(json.dumps({"messages": msgs}, ensure_ascii=False) + "\n")
                        count += 1
        return count

    n_train = _write(train_records, train_path)
    n_eval  = _write(eval_records,  eval_path)

    # Append augmented data to training set (not eval)
    if include_augmented and augmented_path.exists():
        n_aug = 0
        skipped = 0
        raw_rows: list[dict] = []
        for line in augmented_path.open(encoding="utf-8"):
            line = line.strip()
            if not line:
                continue
            try:
                ex = json.loads(line)
            except json.JSONDecodeError:
                skipped += 1
                continue
            tier = (ex.get("_tier") or "gold").lower()
            allowed = {"gold", "bugfix"}
            if include_silver_augmented:
                allowed.add("silver")
            if not gold_only_augmented:
                allowed.add("bronze")
            if tier not in allowed:
                skipped += 1
                continue
            raw_rows.append(ex)

        if augmented_best_per_prompt and raw_rows:
            before = len(raw_rows)
            raw_rows = dedupe_augmented_best_per_prompt(raw_rows)
            print(f"[dataset_builder] augmented best-per-prompt: {before} → {len(raw_rows)} rows")

        with train_path.open("a", encoding="utf-8") as f:
            for ex in raw_rows:
                tier = (ex.get("_tier") or "gold").lower()
                line = json.dumps(ex, ensure_ascii=False)
                repeats = bugfix_oversample if tier == "bugfix" else 1
                for _ in range(max(1, repeats)):
                    f.write(line + "\n")
                    n_aug += 1
        n_train += n_aug
        if skipped:
            print(f"[dataset_builder] augmented filter skipped {skipped} rows (tier not allowed)")
        if bugfix_oversample > 1:
            print(f"[dataset_builder] bugfix_oversample={bugfix_oversample} (bugfix tiers repeated in count above)")
        print(f"[dataset_builder] Appended {n_aug} augmented lines to training set")

    if include_description_sft and description_sft_path.exists():
        n_ds = 0
        n_ds_skip = 0
        with train_path.open("a", encoding="utf-8") as f:
            for line in description_sft_path.open(encoding="utf-8"):
                line = line.strip()
                if not line:
                    continue
                if skip_model_check_shims:
                    try:
                        ex = json.loads(line)
                    except json.JSONDecodeError:
                        f.write(line + "\n")
                        n_ds += 1
                        continue
                    mn = ex.get("_module_name")
                    spec = _extract_final_spec_from_messages(ex)
                    if is_model_check_shim(mn if isinstance(mn, str) else None, spec):
                        n_ds_skip += 1
                        continue
                f.write(line + "\n")
                n_ds += 1
        n_train += n_ds
        msg = f"[dataset_builder] Appended {n_ds} description_sft examples to training set"
        if n_ds_skip:
            msg += f" (skipped {n_ds_skip} MC* TLC shims)"
        print(msg)
    elif include_description_sft:
        print(f"[dataset_builder] Warning: --include-description-sft but {description_sft_path} missing; run scripts/build_description_sft_jsonl.py")

    if include_gold_benchmark and gold_benchmark_path.exists():
        n_gb = 0
        with train_path.open("a", encoding="utf-8") as f:
            for line in gold_benchmark_path.open(encoding="utf-8"):
                line = line.strip()
                if not line:
                    continue
                f.write(line + "\n")
                n_gb += 1
        n_train += n_gb
        print(f"[dataset_builder] Appended {n_gb} gold_benchmark examples to training set")
    elif include_gold_benchmark:
        print(f"[dataset_builder] Warning: --include-gold-benchmark but {gold_benchmark_path} missing; run scripts/build_gold_benchmark_sft.py")

    print(f"[dataset_builder] train={n_train} examples → {train_path}")
    print(f"[dataset_builder] eval ={n_eval}  examples → {eval_path}")
    return n_train, n_eval


