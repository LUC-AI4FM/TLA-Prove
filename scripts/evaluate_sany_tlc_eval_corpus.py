#!/usr/bin/env python3
"""Replay a SANY/TLC-pass eval corpus through the local TLC validator."""
from __future__ import annotations

import argparse
import json
import sys
from dataclasses import asdict, is_dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

REPO = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(REPO))

from src.validators.tlc_validator import validate_string

DEFAULT_CORPUS = REPO / "data" / "processed" / "sany_tlc_pass_eval_v1.jsonl"
DEFAULT_OUT = REPO / "outputs" / "manifests" / "sany_tlc_pass_eval_replay.json"


def _display_path(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(REPO.resolve()))
    except ValueError:
        return str(path)


def _load_jsonl(path: Path) -> list[dict[str, Any]]:
    rows = []
    with path.open(encoding="utf-8") as handle:
        for line in handle:
            if line.strip():
                rows.append(json.loads(line))
    return rows


def _final_content(row: dict[str, Any]) -> str:
    for message in row.get("messages", []):
        if message.get("role") == "assistant" and message.get("channel") == "final":
            return str(message.get("content") or "")
    return ""


def _semantic_summary(semantic: Any) -> dict[str, Any]:
    if is_dataclass(semantic):
        data = asdict(semantic)
    else:
        data = {
            "distinct_states": getattr(semantic, "distinct_states", 0),
            "invariants_checked": getattr(semantic, "invariants_checked", 0),
            "mutation_tested": getattr(semantic, "mutation_tested", False),
            "mutation_caught": getattr(semantic, "mutation_caught", False),
            "trivial_invariant": getattr(semantic, "trivial_invariant", False),
        }
    return {
        "distinct_states": data.get("distinct_states", 0),
        "invariants_checked": data.get("invariants_checked", 0),
        "mutation_tested": data.get("mutation_tested", False),
        "mutation_caught": data.get("mutation_caught", False),
        "trivial_invariant": data.get("trivial_invariant", False),
    }


def replay_corpus(
    *,
    corpus: Path = DEFAULT_CORPUS,
    timeout: int = 60,
    limit: int | None = None,
    require_diamond: bool = False,
) -> dict[str, Any]:
    rows = _load_jsonl(corpus)
    selected = rows[:limit] if limit is not None else rows
    results = []
    failures = []
    gold = 0
    diamond = 0

    for index, row in enumerate(selected, start=1):
        module = str(row.get("_module") or row.get("module") or f"Row{index}")
        final = _final_content(row)
        item: dict[str, Any] = {"index": index, "module": module}
        if not final:
            item.update({"tier": "missing_final", "diamond": False, "semantic": {}})
            failures.append(item)
            results.append(item)
            continue
        try:
            validation = validate_string(final, module_name=module, timeout=timeout)
            item.update(
                {
                    "tier": validation.tier,
                    "diamond": bool(validation.is_diamond),
                    "cfg_was_autogenerated": getattr(validation, "cfg_was_autogenerated", False),
                    "runtime_seconds": getattr(validation, "runtime_seconds", 0.0),
                    "semantic": _semantic_summary(validation.semantic),
                }
            )
            if getattr(validation, "tlc_violations", None):
                item["tlc_violations"] = list(validation.tlc_violations[:3])
            if getattr(validation, "sany_errors", None):
                item["sany_errors"] = list(validation.sany_errors[:3])
        except Exception as exc:  # pragma: no cover - exercised in integration more than unit tests
            item.update({"tier": "exception", "diamond": False, "semantic": {}, "error": str(exc)})

        if item["tier"] == "gold":
            gold += 1
        if item["diamond"]:
            diamond += 1
        if item["tier"] != "gold" or (require_diamond and not item["diamond"]):
            failures.append(item)
        results.append(item)

    checked = len(selected)
    gold_ok = checked > 0 and gold == checked
    diamond_ok = checked > 0 and diamond == checked
    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "ok": gold_ok and (diamond_ok if require_diamond else True),
        "corpus": _display_path(corpus),
        "rows": len(rows),
        "checked": checked,
        "limit": limit,
        "timeout": timeout,
        "require_diamond": require_diamond,
        "gold": gold,
        "diamond": diamond,
        "gold_ok": gold_ok,
        "diamond_ok": diamond_ok,
        "failures": failures,
        "results": results,
    }


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--corpus", type=Path, default=DEFAULT_CORPUS)
    parser.add_argument("--out", type=Path, default=DEFAULT_OUT)
    parser.add_argument("--timeout", type=int, default=60)
    parser.add_argument("--limit", type=int, default=None)
    parser.add_argument("--require-diamond", action="store_true")
    args = parser.parse_args()

    report = replay_corpus(
        corpus=args.corpus,
        timeout=args.timeout,
        limit=args.limit,
        require_diamond=args.require_diamond,
    )
    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(json.dumps(report, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    print(json.dumps(report, indent=2, sort_keys=True))
    return 0 if report["ok"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
